#!/usr/bin/env node

/**
 * CSS Responsive Auto-Adapter
 * 
 * Автоматически добавляет media queries к существующему CSS
 * для адаптации под мобильные устройства
 * 
 * Использование:
 *   node css-responsive-adapter.js input.css output.css
 */

const fs = require('fs');
const path = require('path');

// Конфигурация
const CONFIG = {
  breakpoints: {
    mobile: '768px',
    tablet: '1024px',
    desktop: '1280px'
  },
  
  // Правила для автоматической адаптации
  adaptRules: {
    // Шрифты - уменьшаем на мобильных
    fontSize: (value) => {
      const num = parseFloat(value);
      if (num >= 2) return `${num * 0.7}${value.replace(num, '')}`;
      if (num >= 1.5) return `${num * 0.8}${value.replace(num, '')}`;
      return value;
    },
    
    // Padding - уменьшаем на мобильных
    padding: (value) => {
      const nums = value.match(/[\d.]+/g);
      if (!nums) return value;
      return nums.map(n => {
        const num = parseFloat(n);
        if (num >= 4) return `${num * 0.5}rem`;
        if (num >= 2) return `${num * 0.6}rem`;
        return `${num}rem`;
      }).join(' ');
    },
    
    paddingTop: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    paddingRight: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    paddingBottom: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    paddingLeft: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    
    // Margin - уменьшаем на мобильных
    margin: (value) => CONFIG.adaptRules.padding(value),
    marginTop: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    marginRight: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    marginBottom: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    marginLeft: (value) => CONFIG.adaptRules.padding(value).split(' ')[0],
    
    // Width - 100% на мобильных для больших значений
    width: (value) => {
      if (value.includes('px') && parseFloat(value) > 600) return '100%';
      if (value.includes('%') && parseFloat(value) > 80) return '100%';
      return value;
    },
    
    maxWidth: (value) => {
      if (value.includes('px')) return '100%';
      return value;
    },
    
    // Grid columns - меньше колонок на мобильных
    gridTemplateColumns: (value) => {
      if (value.includes('repeat')) {
        return '1fr';
      }
      return '1fr';
    },
    
    // Flex direction - column на мобильных
    flexDirection: (value) => {
      if (value === 'row') return 'column';
      return value;
    },
    
    // Gap - уменьшаем на мобильных
    gap: (value) => {
      const num = parseFloat(value);
      if (num >= 2) return `${num * 0.5}${value.replace(num, '')}`;
      return value;
    }
  },
  
  // Селекторы которые НЕ нужно адаптировать
  skipSelectors: [
    /^@media/,
    /^@keyframes/,
    /^@import/,
    /^@font-face/,
    /:root/,
    /:before/,
    /:after/
  ]
};

/**
 * Парсит CSS и извлекает правила
 */
function parseCSS(cssContent) {
  const rules = [];
  const regex = /([^{]+)\{([^}]+)\}/g;
  let match;
  
  while ((match = regex.exec(cssContent)) !== null) {
    const selector = match[1].trim();
    const properties = match[2].trim();
    
    // Пропускаем специальные селекторы
    if (CONFIG.skipSelectors.some(pattern => pattern.test(selector))) {
      continue;
    }
    
    // Пропускаем уже существующие media queries
    if (selector.startsWith('@media')) {
      continue;
    }
    
    rules.push({ selector, properties });
  }
  
  return rules;
}

/**
 * Извлекает CSS свойства из строки
 */
function parseProperties(propertiesString) {
  const properties = {};
  const lines = propertiesString.split(';').filter(line => line.trim());
  
  lines.forEach(line => {
    const [prop, value] = line.split(':').map(s => s.trim());
    if (prop && value) {
      properties[prop] = value;
    }
  });
  
  return properties;
}

/**
 * Адаптирует свойства для мобильных устройств
 */
function adaptPropertiesForMobile(properties) {
  const adapted = {};
  
  Object.keys(properties).forEach(prop => {
    const value = properties[prop];
    const camelProp = prop.replace(/-([a-z])/g, (g) => g[1].toUpperCase());
    
    if (CONFIG.adaptRules[camelProp]) {
      adapted[prop] = CONFIG.adaptRules[camelProp](value);
    }
  });
  
  return adapted;
}

/**
 * Генерирует CSS строку из свойств
 */
function propertiesToCSS(properties) {
  return Object.entries(properties)
    .map(([prop, value]) => `  ${prop}: ${value};`)
    .join('\n');
}

/**
 * Генерирует адаптированный CSS
 */
function generateResponsiveCSS(rules) {
  let output = '/* ============================================\n';
  output += '   AUTO-GENERATED RESPONSIVE CSS\n';
  output += '   Generated by CSS Responsive Auto-Adapter\n';
  output += '   ============================================ */\n\n';
  
  // Добавляем оригинальные правила
  output += '/* Original Rules */\n\n';
  rules.forEach(rule => {
    output += `${rule.selector} {\n${rule.properties}\n}\n\n`;
  });
  
  // Добавляем mobile адаптацию
  output += '\n/* ============================================\n';
  output += '   MOBILE ADAPTATIONS (max-width: 768px)\n';
  output += '   ============================================ */\n\n';
  output += '@media (max-width: 768px) {\n';
  
  rules.forEach(rule => {
    const properties = parseProperties(rule.properties);
    const adaptedProperties = adaptPropertiesForMobile(properties);
    
    if (Object.keys(adaptedProperties).length > 0) {
      output += `  ${rule.selector} {\n`;
      output += propertiesToCSS(adaptedProperties) + '\n';
      output += '  }\n\n';
    }
  });
  
  output += '}\n\n';
  
  // Добавляем tablet адаптацию
  output += '/* ============================================\n';
  output += '   TABLET ADAPTATIONS (768px - 1024px)\n';
  output += '   ============================================ */\n\n';
  output += '@media (min-width: 769px) and (max-width: 1024px) {\n';
  output += '  /* Добавьте специфичные правила для планшетов здесь */\n';
  output += '}\n';
  
  return output;
}

/**
 * Основная функция
 */
function main() {
  const args = process.argv.slice(2);
  
  if (args.length < 2) {
    console.log('Usage: node css-responsive-adapter.js <input.css> <output.css>');
    console.log('');
    console.log('Example:');
    console.log('  node css-responsive-adapter.js styles/globals.css styles/globals-responsive.css');
    process.exit(1);
  }
  
  const inputFile = args[0];
  const outputFile = args[1];
  
  console.log('🚀 CSS Responsive Auto-Adapter');
  console.log('================================');
  console.log(`📁 Input:  ${inputFile}`);
  console.log(`📁 Output: ${outputFile}`);
  console.log('');
  
  // Читаем входной файл
  if (!fs.existsSync(inputFile)) {
    console.error(`❌ Error: File ${inputFile} not found`);
    process.exit(1);
  }
  
  const cssContent = fs.readFileSync(inputFile, 'utf-8');
  console.log(`📊 File size: ${(cssContent.length / 1024).toFixed(2)} KB`);
  
  // Парсим CSS
  console.log('⚙️  Parsing CSS...');
  const rules = parseCSS(cssContent);
  console.log(`✅ Found ${rules.length} CSS rules`);
  
  // Генерируем адаптированный CSS
  console.log('🔄 Generating responsive CSS...');
  const responsiveCSS = generateResponsiveCSS(rules);
  
  // Сохраняем результат
  console.log('💾 Saving output...');
  fs.writeFileSync(outputFile, responsiveCSS);
  
  console.log('');
  console.log('✅ Success!');
  console.log(`📁 Output saved to: ${outputFile}`);
  console.log(`📊 Output size: ${(responsiveCSS.length / 1024).toFixed(2)} KB`);
  console.log('');
  console.log('📝 Next steps:');
  console.log('1. Review the generated file');
  console.log('2. Adjust media queries as needed');
  console.log('3. Import in your main CSS file');
  console.log('4. Test on different devices');
}

// Запуск
if (require.main === module) {
  main();
}

module.exports = { parseCSS, adaptPropertiesForMobile, generateResponsiveCSS };
